/*
 * 1.2.13 Avalie a expressão (Math.sqrt
 * (2) * Math.sqrt(2) == 2).
 */
public class Exe13 {
   /**
    * @param args
    */
   public static void main(String[] args) {
      // variable
      double expressao = (Math.sqrt(2) * Math.sqrt(2));
      System.out.println("Expressão = " + expressao);
      System.out.println("A expressão é igual a 2.");
   }
}
